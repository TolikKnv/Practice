from random import *  # Импортируем все функции из модуля random (в частности, choice)

# Список возможных ответов магического шара
answers = [
    "Бесспорно",
    "Предрешено",
    "Никаких сомнений",
    "Определённо да",
    "Можешь быть уверен в этом",
    "Мне кажется - да",
    "Вероятнее всего",
    "Хорошие перспективы",
    "Знаки говорят - да",
    "Да",
    "Пока неясно, попробуй снова",
    "Спроси позже",
    "Лучше не рассказывать",
    "Сейчас нельзя предсказать",
    "Сконцентрируйся и спроси опять",
    "Даже не думай",
    "Мой ответ - нет",
    "По моим данным - нет",
    "Перспективы не очень хорошие",
    "Весьма сомнительно",
]

# Приветственное сообщение
print("Привет, Мир! Я — магический шар, и я знаю ответ на любой твой вопрос.")

# Спрашиваем имя пользователя
name = input("Напиши, как тебя зовут: ")

# Приветствуем по имени
print(f"Привет, {name}!")

# Функция, которая выводит случайный ответ из списка
def answer_to_question():
    print(choice(answers))  # Случайный выбор одного из ответов
    print()
    print("Хочешь спросить у меня ещё что-то?")  # Предлагаем задать новый вопрос

# Основной цикл программы
continue_to_do = "да"  # Переменная для продолжения работы программы
while continue_to_do == "да":  # Пока пользователь отвечает "да"
    print("Напиши свой вопрос:\n")
    input()  # Пользователь вводит вопрос (текст не используется, важен сам факт)
    answer_to_question()  # Выводим случайный ответ
    continue_to_do = input()  # Считываем ответ пользователя: хочет ли он продолжить

# Когда пользователь вводит что-то кроме "да", программа завершается
print("Возвращайся, если возникнут вопросы!")
